const dictionaries: Record<Lang, Dict> = { en, es }

interface I18nContextValue {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: string, vars?: Record<string, string | number>) => string
}

function interpolate(str: string, vars?: Record<string, string | number>): string {
  if (!vars) return str
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? String(vars[k]) : `{${k}}`))
}

export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  return interpolate(dictionaries[lang][key] ?? dictionaries.en[key] ?? key, vars)
}

const I18nContext = createContext<I18nContextValue | null>(null)
const STORAGE_KEY = 'freelancer_lang'

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem(STORAGE_KEY)
    return saved === 'es' ? 'es' : 'en'
  })

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, lang)
    document.documentElement.lang = lang
  }, [lang])

  const value = useMemo<I18nContextValue>(() => ({
    lang,
    setLang: setLangState,
    t: (key: string, vars?: Record<string, string | number>) =>
      interpolate(dictionaries[lang][key] ?? dictionaries.en[key] ?? key, vars),
  }), [lang])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
